function Ask_group_delete(id)
{
    var answer = confirm("Czy napewno usunąć wybrany element i powiązane z nim obiekty?");
    if (!answer)
        return window.location=("http://localhost:3000/group/list_groups");

    else
        return window.location=("http://localhost:3000/group/delete_group/"+id);

    end
}

